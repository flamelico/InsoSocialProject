package insosocialproject;


public class PrivacidadGrupo {
    
    private boolean edadregis, userregis;
    private int vecesdenun;

    public boolean isEdadregis() {
        return edadregis;
    }

    public void setEdadregis(boolean edadregis) {
        this.edadregis = edadregis;
    }

    public boolean isUserregis() {
        return userregis;
    }

    public void setUserregis(boolean userregis) {
        this.userregis = userregis;
    }

    public int getVecesdenun() {
        return vecesdenun;
    }

    public void setVecesdenun(int vecesdenun) {
        this.vecesdenun = vecesdenun;
    }
    
}
